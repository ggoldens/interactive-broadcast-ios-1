#import "UIColor+AppAdditions.h"

@implementation UIColor (AppAdditions)

+ (UIColor *)BarColor{
    return [UIColor colorWithRed:0.961 green:0.961 blue:0.961 alpha:1];
}

+ (UIColor *)SLBlueColor{
    return [UIColor colorWithRed:0.004 green:0.635 blue:0.89 alpha:1];
}

+ (UIColor *)SLRedColor{
    return [UIColor colorWithRed:0.941 green:0.275 blue:0.176 alpha:1];
}

+ (UIColor *)SLGreenColor{
    return [UIColor colorWithRed:0.149 green:0.651 blue:0.357 alpha:1];
}

+ (UIColor *)SLGrayColor{
    return [UIColor colorWithRed:0.37 green:0.38 blue:0.38 alpha:1.0];
}

@end
